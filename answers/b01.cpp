#include <iostream>

void solve(std::istream& is, std::ostream& os) {
    int a {0};
    int b {0};
    is >> a >> b;
    os << (a + b) << "\n";
    return;
}

int main(void) {
    solve(std::cin, std::cout);
    return 0;
}

